public class AssignmentOperator 
{
	public static void main(String[]args)
	{
		int a = 13;
	    a+=14;
	    System.out.println(a);
	    int b = 15;
	    b-=5;
	    System.out.println(b);
	    int c = 23;
	    c*=5;
	    System.out.println(c);
	    int d = 46;
	    d/=15;
	    System.out.println(d);
	    int m =15;
	    m%=10;
	    System.out.println(m);

	}
}
