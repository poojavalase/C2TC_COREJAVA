public class ArithmeticOperators 
{
	public static void main(String[]args)
	{
		int a = 47;
		int b = 64;
		int A = a+b;
		int M = a*b;
		int D = a-b;
		int S = a/b;
		System.out.println("Addition :" +A);
		System.out.println("multiplication  : "+M);
		System.out.println("substraction : "+D);
		System.out.println("Division : "+S);
		
	}

}

