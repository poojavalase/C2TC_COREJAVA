public class Typecasting
 {

	public static void main(String[] args) {
		
       float f = 10.532f;
       long l = (long) f;
       System.out.println(l);
       byte b = 48;
       int i = (int) b;
       System.out.println(i);
       short I = b; 
       System.out.println(I);
       long L = (long)b;
       System.out.println(L);
       float F = (float) b;	
       System.out.println(F);
       double d = (double) b;
       System.out.println(d);
       }
}

