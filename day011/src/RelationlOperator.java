public class RelationlOperator 
{
	public static void main(String[]args)
	{
		int a = 24;
		int b = 10;
		boolean c = a>b;
		boolean d = a<b;
		System.out.println(c);
		System.out.println(d);
		int x = 34;
		int y = 45;
		System.out.println("x==y : "+(x==y));
		System.out.println("x>y : "+(x>y));
		System.out.println("x<y : "+(x<y));
		System.out.println("x!=y : "+(x!=y));
		System.out.println("x>=y : "+(x>=y));
		System.out.println("x<=y : "+(x<=y));
	}

}
